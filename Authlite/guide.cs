1. Подготовка и Модель Данных
C#
using System.Collections.Generic;
using System.Linq;
using System.Windows;
using ... — это подключение библиотек. Collections.Generic нужен для работы со списками (List), Linq дает нам крутые команды для быстрого поиска по массивам (FirstOrDefault, Any), а Windows нужен для работы с интерфейсом (например, для вывода окошек MessageBox).

C#
namespace SimpleAuthApp
namespace — это пространство имен. Представь, что это папка или "коробка", в которой лежит весь наш проект, чтобы он не конфликтовал с другими программами.

C#
public class User
{
    public string Username { get; set; }
    public string Password { get; set; }
}
public class User — мы создаем чертеж (модель) пользователя. У каждого пользователя в системе обязательно должно быть имя (Username) и пароль (Password). get; set; означает, что эти данные можно как читать, так и записывать (изменять).

2. Главное окно и "База данных"
C#
public partial class LoginWindow : Window
Здесь начинается класс самого окна. Он наследуется от базового окна WPF (: Window). Слово partial означает, что этот код — только половина окна (вторая половина — это визуальный дизайн в XAML).

C#
private static List<User> _usersDb = new List<User>();
private — переменная доступна только внутри этого окна.

static — очень важное слово! Оно означает, что этот список принадлежит не конкретному открытому окошку, а классу в целом. Данные из него не удалятся, даже если окно закроется и откроется снова (пока работает программа).

List<User> — создаем пустой список (массив), куда будем складывать наших пользователей.

3. Запуск окна (Конструктор)
C#
public LoginWindow()
