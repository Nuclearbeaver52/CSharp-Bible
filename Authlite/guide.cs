Проверяем: если наш список пустой (Count == 0), то мы вручную добавляем (Add) туда первого "тестового" пользователя admin с паролем 123. Это делается для того, чтобы при запуске тебе было под кем тестировать вход.

4. Кнопка "Войти" (Авторизация)
C#
private void LoginButton_Click(object sender, RoutedEventArgs e)
Это метод, который срабатывает, когда пользователь кликает на кнопку "Войти".

C#
    string inputLogin = LoginTextBox.Text;
    string inputPassword = PassBox.Password;
Берем то, что ввел человек в поле логина (Text) и в специальном поле пароля (Password), и сохраняем в две строковые переменные inputLogin и inputPassword.

C#
    if (string.IsNullOrWhiteSpace(inputLogin) || string.IsNullOrWhiteSpace(inputPassword))
    {
        MessageBox.Show("Пожалуйста, заполните все поля!", ...);
        return;
    }
string.IsNullOrWhiteSpace — отличная проверка. Она смотрит, не пустое ли поле и не ввел ли пользователь просто пробелы. Если (|| — логическое ИЛИ) хотя бы одно поле пустое, показываем ошибку и делаем return (то есть немедленно прерываем выполнение метода, дальше код не идет).

C#
    var user = _usersDb.FirstOrDefault(u => u.Username == inputLogin && u.Password == inputPassword);
Самая главная строчка (Магия LINQ). FirstOrDefault пробегается по нашему списку _usersDb и проверяет каждого пользователя (которого мы на время проверки назвали u). Он ищет того, у кого Имя совпадает с введенным логином И (&&) Пароль совпадает с введенным паролем.

Если находит идеальное совпадение — кладет этого пользователя в переменную user. Если совпадений нет — user становится пустым (null).

C#
    if (user != null) 
    { 
        // Если user не пустой — значит совпадение найдено. Выводим успех.
    } 
    else 
    { 
        // Если null — значит логин или пароль не подошли. Выдаем ошибку доступа.
    }
}
5. Кнопка "Регистрация"
C#
private void RegisterButton_Click(object sender, RoutedEventArgs e)
Метод для клика по кнопке регистрации. Первые строчки здесь такие же: считываем текст и проверяем на пустоту через IsNullOrWhiteSpace.

C#
    bool isLoginTaken = _usersDb.Any(u => u.Username == inputLogin);
Any — это тоже метод LINQ. Он ищет в списке пользователя, у которого имя совпадает с введенным логином. Метод просто отвечает на вопрос «Есть ли хоть кто-то подходящий под условие?». Если находит — возвращает true (да, занят).

C#
    if (isLoginTaken)
    {
        MessageBox.Show("Пользователь с таким логином уже существует!", ...);
    }
Если логин занят (true), ругаемся и не даем создать дубликат.

C#
    else
    {
        _usersDb.Add(new User { Username = inputLogin, Password = inputPassword });
        MessageBox.Show("Вы успешно зарегистрированы!", ...);
        PassBox.Password = "";
    }
}
Если логин свободен (else), мы берем введенные данные, формируем из них нового пользователя (new User { ... }) и добавляем его в наш массив с помощью метода Add().

В самом конце мы принудительно очищаем поле с паролем PassBox.Password = "", чтобы он не оставался висеть на экране из соображений безопасности.
